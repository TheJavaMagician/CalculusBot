package calc.lib;

/** Class (POJO) used to store Co-ordinates JSON objects
 *
 * @author Carlin Robertson
 * @version 1.0
 *
 */
public class Coords {
    private String z;

    private String y;

    private String x;

    public String getZ ()
    {
        return z;
    }

    public void setZ (String z)
    {
        this.z = z;
    }

    public String getY ()
    {
        return y;
    }

    public void setY (String y)
    {
        this.y = y;
    }

    public String getX ()
    {
        return x;
    }

    public void setX (String x)
    {
        this.x = x;
    }

}
